package com.zhukai.framework.fast.rest.exception;

public class SetupInitException extends Exception {

    public SetupInitException() {
        super();
    }

    public SetupInitException(Throwable cause) {
        super(cause);
    }

    public SetupInitException(String message) {
        super(message);
    }

    public SetupInitException(String message, Throwable cause) {
        super(message, cause);
    }
}
